package co.grandcircus.vitamenu.model;

public class Vitamin {

	private String vitaminLetter;
	private String recIntake;
	
	public String getVitaminLetter() {
		return vitaminLetter;
	}
	public void setVitaminLetter(String vitaminLetter) {
		this.vitaminLetter = vitaminLetter;
	}
	public String getRecIntake() {
		return recIntake;
	}
	public void setRecIntake(String recIntake) {
		this.recIntake = recIntake;
	}


	
}
